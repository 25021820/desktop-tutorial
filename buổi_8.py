class Triangle:
    def __init__(self, e1, e2, e3):
        self.e1 = e1
        self.e2 = e2
        self.e3 = e3

    def getArea(self):
        if self.e1 + self.e2 <= self.e3 or \
           self.e1 + self.e3 <= self.e2 or \
           self.e2 + self.e3 <= self.e1:
            return -1

        s = (self.e1 + self.e2 + self.e3) / 2
        area = math.sqrt(s*(s-self.e1)*(s-self.e2)*(s-self.e3))
        return area


a, b, c = map(int, input().split())

t = Triangle(a, b, c)
area = t.getArea()

