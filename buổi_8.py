import math

def cross(u, v):
    return [
        u[1]*v[2] - u[2]*v[1],
        u[2]*v[0] - u[0]*v[2],
        u[0]*v[1] - u[1]*v[0]
    ]

def dot(u, v):
    return u[0]*v[0] + u[1]*v[1] + u[2]*v[2]

def length(v):
    return math.sqrt(dot(v, v))


A = list(map(float, input().split()))
B = list(map(float, input().split()))
C = list(map(float, input().split()))
D = list(map(float, input().split()))

