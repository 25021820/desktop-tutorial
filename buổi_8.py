class Point:
    def __init__(self, x, y):
        self.x = x
        self.y = y

    def is_origin(self):
        return self.x == 0 and self.y == 0

    def on_y_axis(self):
        return self.x == 0 and self.y != 0

    def on_x_axis(self):
        return self.y == 0 and self.x != 0

    def distance_to_origin(self):
        return math.sqrt(self.x*self.x + self.y*self.y)


