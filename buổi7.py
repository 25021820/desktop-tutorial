def max_subarray_mod(nums, m):
    n = len(nums)
    max_value = 0

    for i in range(n):
        current_sum = 0
        for j in range(i, n):
            current_sum += nums[j]
            value = current_sum % m
            if value > max_value:
                max_value = value

    return max_value

def count_word(sentence, keyword):
    count = 0
    n = len(sentence)
    m = len(keyword)

    for i in range(n - m + 1):
        if sentence[i:i+m] == keyword:
            count += 1

    return count

