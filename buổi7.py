def longest_harmonious(arr):
    max_len = 0
    for i in range(len(arr)):
        count1 = 0
        count2 = 0
        for j in range(len(arr)):
            if arr[j] == arr[i]:
                count1 += 1
            if arr[j] == arr[i] + 1:
                count2 += 1
        if count1 > 0 and count2 > 0:
            if count1 + count2 > max_len:
                max_len = count1 + count2
    return max_len

