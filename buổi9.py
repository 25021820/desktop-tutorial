def findRadius(houses, heaters):
    houses.sort()
    heaters.sort()

    max_radius = 0

    for h in houses:
        min_dist = float('inf')

        for t in heaters:
            dist = abs(h - t)
            if dist < min_dist:
                min_dist = dist

        if min_dist > max_radius:
            max_radius = min_dist

    return max_radius


