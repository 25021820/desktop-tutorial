def is_path_crossing(moves):
    x = 0
    y = 0

    visited = set()
    visited.add((0, 0))

    for m in moves:
        if m == 'R':
            x += 1
        elif m == 'L':
            x -= 1
        elif m == 'U':
            y += 1
        elif m == 'D':
            y -= 1

        if (x, y) in visited:
            return True

        visited.add((x, y))

    return False


